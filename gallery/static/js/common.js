function showMessage(msg, timeout, head){
  showMessageOrError(false, msg, timeout, head);
}

function showError(msg, timeout, head){
  showMessageOrError(true, msg, timeout, head);
}

function showMessageOrError(isError, msg, timeout, head){
  if(!timeout) timeout = 5000;
  if(!head){
    head = "";
  }else{
    head = '<h4 class="alert-heading">'+head+'</h4>';
  }
  var alertType = isError?"alert-danger":"alert-success";
  var template='<div id="div-global-message" style="position:fixed; top: 60px; width: 100%; display:none; height: 0"><div class="container" style="height:0"><div class="row" style="height:0"><div class="alert alert-block $(0) pull-right" style="width: 20%;"><button data-dismiss="alert" class="close" type="button">Ã—</button>$(1)<p>$(2)</p></div></div></div></div>';  
  $("#div-global-message").remove();
  $(template.replace("$(0)", alertType).replace("$(1)", head).replace("$(2)", msg)).appendTo('body').fadeIn();
  setTimeout(function(){$("#div-global-message").fadeOut();}, timeout)
}

